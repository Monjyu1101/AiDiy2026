def main():
    tts = TextToSpeech()
    total = len(DIALOGUES)
    done  = 0
    skip  = 0
    fail  = 0

    for scene, turn, gender, text in DIALOGUES:
        fname = f"dlg_{scene}_{turn:02d}_{gender}.mp3"
        fpath = os.path.join(OUTPUT_DIR, fname)

        # 既存ファイルをスキップ（500バイト超）
        if os.path.exists(fpath) and os.path.getsize(fpath) > 500:
            print(f"  [SKIP] {fname}")
            skip += 1
            continue

        print(f"  [GEN ] {fname}")
        try:
            audio_bytes, _ = tts.synthesize(
                speech_text=text,
                language="ja",
                provider="edge",
                voice=gender,
            )
            with open(fpath, "wb") as f:
                f.write(audio_bytes)
            size = len(audio_bytes)
            if size > 500:
                print(f"         -> OK ({size:,} bytes)")
                done += 1
            else:
                print(f"         -> FAIL (empty or too small)")
                fail += 1
        except Exception as e:
            print(f"         -> ERROR: {e}")
            fail += 1

    print(f"\n完了: {done} 生成, {skip} スキップ, {fail} 失敗 (合計 {total} 件)")


if __name__ == "__main__":
    main()
