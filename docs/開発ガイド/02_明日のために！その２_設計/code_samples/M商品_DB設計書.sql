-- ------------------------------------------------------------
-- 1. テーブル概要
-- ------------------------------------------------------------
-- テーブル名: M商品
-- 主キー: 商品ID
-- 概要: 商品マスタ情報を管理する
-- 備考:
--   - 商品分類ID で M商品分類 と紐づく
--   - 有効=false による論理削除を採用
--   - 監査フィールド8項目を含む
-- ------------------------------------------------------------

-- ------------------------------------------------------------
-- 2. テーブル定義
-- ------------------------------------------------------------
CREATE TABLE M商品 (
    商品ID TEXT PRIMARY KEY,
    商品名 TEXT NOT NULL,
    単位 TEXT NOT NULL,
    商品分類ID TEXT NOT NULL,
    商品備考 TEXT,
    有効 BOOLEAN NOT NULL DEFAULT 1,
    登録日時 TEXT NOT NULL,
    登録利用者ID TEXT NOT NULL,
    登録利用者名 TEXT,
    登録端末ID TEXT NOT NULL,
    更新日時 TEXT NOT NULL,
    更新利用者ID TEXT NOT NULL,
    更新利用者名 TEXT,
    更新端末ID TEXT NOT NULL
);

-- ------------------------------------------------------------
-- 3. カラム定義
-- ------------------------------------------------------------
-- 商品ID       : TEXT / PK / 商品を一意に識別するID
-- 商品名       : TEXT / NOT NULL / 商品名
-- 単位         : TEXT / NOT NULL / Kg、個、箱など
-- 商品分類ID   : TEXT / NOT NULL / M商品分類.商品分類ID を参照
-- 商品備考     : TEXT / NULL / 備考
-- 有効         : BOOLEAN / NOT NULL / 有効フラグ。削除時は false
-- 登録日時     : TEXT / NOT NULL / 登録日時
-- 登録利用者ID : TEXT / NOT NULL / 登録利用者ID
-- 登録利用者名 : TEXT / NULL / 登録利用者名
-- 登録端末ID   : TEXT / NOT NULL / 登録端末ID
-- 更新日時     : TEXT / NOT NULL / 更新日時
-- 更新利用者ID : TEXT / NOT NULL / 更新利用者ID
-- 更新利用者名 : TEXT / NULL / 更新利用者名
-- 更新端末ID   : TEXT / NOT NULL / 更新端末ID

-- ------------------------------------------------------------
-- 4. V商品 一覧用SQL（概要）
-- ------------------------------------------------------------
-- V商品 は DB VIEW オブジェクトではなく、apps_router/V商品.py の生SQLで実装する。
-- 商品分類名を M商品分類 から LEFT JOIN し、一覧画面では通常こちらを使う。

SELECT
    M.商品ID,
    M.商品名,
    M.単位,
    M.商品分類ID,
    C.商品分類名,
    M.商品備考,
    M.有効,
    M.登録日時,
    M.登録利用者ID,
    M.登録利用者名,
    M.登録端末ID,
    M.更新日時,
    M.更新利用者ID,
    M.更新利用者名,
    M.更新端末ID
FROM M商品 M
LEFT JOIN M商品分類 C ON M.商品分類ID = C.商品分類ID
WHERE M.有効 = 1
ORDER BY M.商品ID
LIMIT 1000;

