-- 8. 削除方式
-- ------------------------------------------------------------
-- 物理削除ではなく、有効=false へ更新する。
-- 更新時は更新日時・更新利用者ID・更新利用者名・更新端末ID も更新する。

UPDATE M商品
SET
    有効 = 0,
    更新日時 = '2026-04-23 10:30:00',
    更新利用者ID = 'admin',
    更新利用者名 = '管理者',
    更新端末ID = 'localhost'
WHERE 商品ID = 'A001';

